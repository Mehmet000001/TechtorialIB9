package Loops;

public class ForLoopPractice1 {
    public static void main(String[] args) {
        // find the sum of number in btwn 0 to 5
        int sum=0;
        for (int a=0;a<=5;a++){
            System.out.println(a);
            sum=sum+a;
        }
        System.out.println(sum);

    }
}
