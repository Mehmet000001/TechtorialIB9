package Loops;

public class ForLoopMultipleTerms {
    public static void main(String[] args) {

        for (int k=5,t=10,m=25;k<t && t>0; k++,t--){///   || more run
            m++;
            System.out.println("T is: "+k);
            System.out.println("T is "+t);
            System.out.println("this is: "+m);
        }

    }
}

