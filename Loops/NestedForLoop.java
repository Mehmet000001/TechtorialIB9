package Loops;

public class NestedForLoop {
    public static void main(String[] args) {
        for (int i=0;i<3;i++){

            for (int k=0;k<5;k++){
                System.out.println("this is i:"+i);///first second block run after done go to first block
                System.out.println("this is k:"+k);


            }

        }
    }
}
